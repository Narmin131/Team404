// Data types 

// Primitive - Besit verilen tipi

// String data type

var a = "Ilkin"
console.log(a);

// Number data type

var b = 12;
console.log(b);
console.log("12" + "12");

console.log('My name is : ' + a + ' My age is :' + b );

// Boolean data type

var c = false;
var d = true;
console.log(c);
console.log(d);


// Null data type

var e = null;
console.log(e);

// Undefined data type 
 
var f;
console.log(f);

// Non-primitive (Composite) - Murekkeb verilen tipi

// Object data type

var myObject = {
    ad : 'Anar', 
    yas : 15,
    seher: 'Sumqayit'
}
// Butun obyekti konsola yazdirmaq
console.log(myObject);

// Obyektin elementlerini ayrica yazdirmaq
console.log(myObject.seher);
console.log(myObject.yas);
console.log(myObject.ad);

// Array data type

var myArray = [true, "Ilkin", 124, false, "Tural"]

// Butun massivi konsola yazdirmaq
console.log(myArray);

// Massivin 3 cu indeksinde yerlesen elementi konsola yazdirmaq
console.log(myArray[3]);

